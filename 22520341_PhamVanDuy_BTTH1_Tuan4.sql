CREATE DATABASE QLBH 
USE QLBH

CREATE TABLE KHACHHANG
(
	MAKH char(4) PRIMARY KEY, 
	HOTEN varchar(40),
	DCHI varchar(50), 
	SODT varchar(20),
	NGSINH smalldatetime, 
	DOANHSO money, 
	NGDK smalldatetime
)

CREATE TABLE SANPHAM
(
	MASP char(4) PRIMARY KEY, 
	TENSP varchar(40) ,
	DVT varchar(20) , 
	NUOCSX varchar(40), 
	GIA money
)

CREATE TABLE NHANVIEN 
(
	MANV char(4) PRIMARY KEY, 
	HOTEN varchar(40), 
	SODT varchar(20), 
	NGVL smalldatetime
)

CREATE TABLE HOADON
(
	SOHD int PRIMARY KEY, 
	NGHD smalldatetime, 
	MAKH char(4) CONSTRAINT HD_MAKH_FK FOREIGN KEY (MAKH) REFERENCES KHACHHANG(MAKH), 
	MANV char(4) CONSTRAINT HD_MANV_FK FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV),
	TRIGIA money, 
)

CREATE TABLE CTHD
( 
	SOHD int CONSTRAINT CTHD_SOHD_FK FOREIGN KEY (SOHD) REFERENCES HOADON(SOHD), 
	MASP char(4) CONSTRAINT CTHD_MASP_FK FOREIGN KEY (MASP) REFERENCES SANPHAM(MASP),
	CONSTRAINT CTHD_SOHD_MASP_PK PRIMARY KEY (SOHD,MASP),
	SL int
)

